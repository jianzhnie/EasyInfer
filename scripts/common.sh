#!/usr/bin/env bash
# ==============================================================================
# 共享工具函数库 (common.sh)
# 供 scripts/ 下各子目录的脚本 source 使用
# ==============================================================================

# 颜色常量
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
CYAN='\033[0;36m'
NC='\033[0m'

# ------------------------------------------------------------------------------
# 日志函数
# ------------------------------------------------------------------------------
log_info()  { echo -e "${GREEN}[INFO]${NC}  $(date '+%Y-%m-%d %H:%M:%S') - $*"; }
log_warn()  { echo -e "${YELLOW}[WARN]${NC}  $(date '+%Y-%m-%d %H:%M:%S') - $*" >&2; }
log_err()   { echo -e "${RED}[ERROR]${NC} $(date '+%Y-%m-%d %H:%M:%S') - $*" >&2; }
log_fatal() { echo -e "${RED}[FATAL]${NC} $(date '+%Y-%m-%d %H:%M:%S') - $*" >&2; exit 1; }

# ------------------------------------------------------------------------------
# 节点列表读取
# ------------------------------------------------------------------------------
read_nodes() {
    local nodes_file="${1:?用法: read_nodes <nodes_file>}"
    if [[ ! -f "$nodes_file" ]]; then
        log_err "节点列表文件未找到: $nodes_file"
        return 1
    fi
    awk 'NF && !/^#/ {print $1}' "$nodes_file"
}

# ------------------------------------------------------------------------------
# SSH 辅助函数
# ------------------------------------------------------------------------------
ssh_target() {
    printf "%s%s" "${SSH_USER_HOST_PREFIX:-}" "$1"
}

ssh_run() {
    local node="$1"; shift
    ssh ${SSH_OPTS:-} "$(ssh_target "$node")" "$@"
}

# ------------------------------------------------------------------------------
# 并发控制
# ------------------------------------------------------------------------------
limit_jobs() {
    local max="${1:?用法: limit_jobs <max>}"
    while [[ "$(jobs -rp 2>/dev/null | wc -l)" -ge "$max" ]]; do
        wait -n 2>/dev/null || sleep 0.1
    done
}

# ------------------------------------------------------------------------------
# 网络工具：获取业务网卡 IP
# ------------------------------------------------------------------------------
get_node_ip() {
    local interface="${1:?用法: get_node_ip <interface>}"
    local ip=""
    if command -v ip >/dev/null 2>&1; then
        ip=$(ip -4 addr show "${interface}" 2>/dev/null | awk '/inet / {print $2}' | cut -d/ -f1 | head -n 1)
    elif command -v ifconfig >/dev/null 2>&1; then
        ip=$(ifconfig "${interface}" 2>/dev/null | awk '/inet / {print $2}' | head -n 1)
    fi
    echo "$ip"
}

# ------------------------------------------------------------------------------
# scripts 目录路径 (基于 common.sh 的位置)
# ------------------------------------------------------------------------------
SCRIPTS_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
